class Subject {
  final String name;
  final String code;
  final String timeSchedule;
  final String daySchedule;
  final String desc;
  final String teacher;
  final String room;

  Subject({
    required this.name,
    required this.code,
    required this.timeSchedule,
    required this.daySchedule,
    required this.desc,
    required this.teacher,
    required this.room,
  });
}
